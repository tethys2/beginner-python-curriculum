print(4 + 5) # Addition: adds two numbers

print(10 - 3) # Subtractions: subtracts the second number from the first number

print(6 * 7)  # Multiplication: multiplies two numbers

print(15 / 2) # Division: divides and gives a decimal result

print(15 // 2) # Floor division: divide and round down

print(15 % 2) # Modulus: gives the remainder

print(2 ** 3) # Exponentiation: first number raised to the second number

print(2 + 3 * 4) # 14 (3 * 4 = 12, then 2 + 12 = 14)
print((2 + 3) * 4) # 20 (2 + 3 = 5, then 5 * 4 = 20)

